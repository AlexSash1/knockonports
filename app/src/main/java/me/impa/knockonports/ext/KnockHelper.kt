package me.impa.knockonports.ext

import android.content.Context
import android.content.Intent
import android.os.Build
import me.impa.knockonports.service.KnockerService

fun Context.startSequence(sequenceId: Long) {
    val intent = Intent(this, KnockerService::class.java)
    intent.putExtra(KnockerService.SEQUENCE_ID, sequenceId)
    if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
        this.startForegroundService(intent)
    } else {
        this.startService(intent)
    }
}